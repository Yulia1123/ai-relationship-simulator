export function getArchetypeResult(id: string, fit: number): LoveArchetypeResult {
  const a = ARCHETYPES.find((x) => x.id === id)!;
  return {
    id: a.id,
    name: a.name,
    slogan: a.slogan,
    tagline: a.tagline,
    lee: a.lee,
    attachment: a.attachment,
    portrait: a.portrait,
    spark: a.spark,
    flip: a.flip,
    advice: a.advice,
    fit,
  };
}

// 由 Lee 风格 + 依恋类型定位主型动物
export function getAnimalByGrid(lee: LeeStyle, attachment: AttachmentType): Archetype {
  return ARCHETYPES.find((a) => a.lee === lee && a.attachment === attachment)!;
}

